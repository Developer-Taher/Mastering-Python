# الطريقة الأولى: استخدام حلقة for
total = 0
for i in range(1, 1000001):
    total += i
print(f"مجموع الأرقام من 1 إلى مليون هو: {total}")

# الطريقة الثانية: استخدام الدالة sum مع range (أسرع)
total = sum(range(1, 1000001))
print(f"مجموع الأرقام من 1 إلى مليون هو: {total}")

# الطريقة الثالثة: استخدام القانون الرياضي (الأسرع على الإطلاق)
n = 1000000
total = n * (n + 1) // 2
print(f"مجموع الأرقام من 1 إلى مليون هو: {total}")


