#------------------------------------------
#type() to get the data type of the object
#all data in python is object 
#------------------------------------------
print(type(19))             #int
print(type(100))            #int
print(type(-50))            #int

print(type(10.5))           #float
print(type(1.92))           #float
print(type(-21.92))         #float

print(type('i love python'))#string
print(type([1,2,3,45,6,3])) #list
print(type((1,2,3,4,5)))    #tuple

print(type({"one":1,"two":2,"three":3})) #dict

print(type(2==3))                        #boolean